def reset_game():
    """
    Returns a dict of all game state so we avoid OOP and keep it understandable.
    """
    return {
        "mode": "INTRO",          # INTRO, STAGE, FEEDBACK, GAME_OVER, CLEAR
        "intro_index": 0,         # which intro line
        "stage_index": 0,         # current stage
        "feedback": None,         # ("good"/"bad", message)
        "await_choice": False,    # if in stage and choices active
        "last_result": None,      # track last correctness
    }

def advance_intro(state):
    state["intro_index"] += 1
    if state["intro_index"] >= len(INTRO_LINES):
        state["mode"] = "STAGE"
        state["await_choice"] = True
    return state

def current_stage(state):
    return STAGES[state["stage_index"]]

def handle_choice(state, choice_num):
    """
    choice_num is 1/2/3 from key press.
    """
    st = current_stage(state)
    chosen_idx = choice_num - 1
    correct = (chosen_idx == st["correct_index"])

    if correct:
        state["mode"] = "FEEDBACK"
        state["feedback"] = ("good", MSG_CORRECT)
        state["last_result"] = True
    else:
        state["mode"] = "FEEDBACK"
        state["feedback"] = ("bad", MSG_WRONG)
        state["last_result"] = False
    state["await_choice"] = False
    return state

def advance_after_feedback(state):
    """
    After showing '효과는...' or '고객이 떠났습니다...' for ENTER,
    move to next stage or game over.
    """
    if state["last_result"] is True:
        # go next stage or clear
        state["stage_index"] += 1
        if state["stage_index"] >= len(STAGES):
            state["mode"] = "CLEAR"
        else:
            state["mode"] = "STAGE"
            state["await_choice"] = True
    else:
        state["mode"] = "GAME_OVER"
    state["feedback"] = None
    return state

