def load_fonts():
    """
    Load fonts with fallbacks.
    For a more 8-bit look, you can later ship a pixel TTF and load it here.
    """
    # NOTE: pygame's default font is okay; we simulate retro through layout/colors.
    base = pygame.font.Font(None, 28)
    small = pygame.font.Font(None, 22)
    big = pygame.font.Font(None, 36)
    return base, small, big

def wrap_text(text, font, max_width):
    """Simple word-wrapping (Korean spacing is variable; this still works decently)."""
    words = text.split(" ")
    if not words:
        return [""]

    lines = []
    current = words[0]
    for w in words[1:]:
        test = current + " " + w
        if font.size(test)[0] <= max_width:
            current = test
        else:
            lines.append(current)
            current = w
    lines.append(current)
    return lines

def draw_box(surface, rect, fill, border, border_thickness=3, radius=12):
    pygame.draw.rect(surface, fill, rect, border_radius=radius)
    pygame.draw.rect(surface, border, rect, width=border_thickness, border_radius=radius)

def draw_text_lines(surface, lines, font, color, x, y, line_gap=6):
    yy = y
    for line in lines:
        img = font.render(line, True, color)
        surface.blit(img, (x, yy))
        yy += img.get_height() + line_gap

