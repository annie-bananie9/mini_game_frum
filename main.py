def main():
    pygame.init()
    pygame.display.set_caption("KIA CXJ Quest (Pygame)")
    screen = pygame.display.set_mode((WIDTH, HEIGHT))
    clock = pygame.time.Clock()
    fonts = load_fonts()

    state = reset_game()

    running = True
    while running:
        dt = clock.tick(FPS)

        # Events
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                running = False

            if event.type == pygame.KEYDOWN:
                if event.key == pygame.K_ESCAPE:
                    running = False

                # Restart
                if event.key == pygame.K_r and state["mode"] in ("GAME_OVER", "CLEAR"):
                    state = reset_game()

                # Advance (Enter/Space)
                if event.key in (pygame.K_RETURN, pygame.K_SPACE):
                    if state["mode"] == "INTRO":
                        state = advance_intro(state)
                    elif state["mode"] == "FEEDBACK":
                        state = advance_after_feedback(state)
                    elif state["mode"] == "STAGE":
                        # In stage, Enter doesn't do anything (must pick 1/2/3)
                        pass

                # Choices (1/2/3)
                if state["mode"] == "STAGE" and state["await_choice"]:
                    if event.key == pygame.K_1:
                        state = handle_choice(state, 1)
                    elif event.key == pygame.K_2:
                        state = handle_choice(state, 2)
                    elif event.key == pygame.K_3:
                        state = handle_choice(state, 3)

        # Render
        screen.fill(COLOR_BG)

        if state["mode"] == "INTRO":
            render_intro(screen, fonts, state)
        elif state["mode"] == "STAGE":
            render_stage(screen, fonts, state)
        elif state["mode"] == "FEEDBACK":
            render_feedback(screen, fonts, state)
        elif state["mode"] == "GAME_OVER":
            render_game_over(screen, fonts, state)
        elif state["mode"] == "CLEAR":
            render_clear(screen, fonts, state)

        pygame.display.flip()

    pygame.quit()
    sys.exit(0)

if __name__ == "__main__":
    main()
